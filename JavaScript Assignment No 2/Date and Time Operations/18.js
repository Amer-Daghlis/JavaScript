// Program to Compare the Value of Two Dates

let date1 = new Date('2024-09-13');
let date2 = new Date('2024-12-31');
if (date1 > date2) {
    console.log("Date1 is later");
} else if (date1 < date2) {
    console.log("Date2 is later");
} else {
    console.log("Both dates are equal");
}

